const num1 = 2;
const num2 = 3;
const oper = "-";

if (oper === "-") {
    console.log(num1 - num2);
} else if (oper === "+") {
    console.log(num1 + num2);
} else if (oper === "/") {
    console.log(num1 / num2);
} else if (oper === "*") {
    console.log(num1 * num2);
} else {
    console.log("Insira uma operação válida!")
}